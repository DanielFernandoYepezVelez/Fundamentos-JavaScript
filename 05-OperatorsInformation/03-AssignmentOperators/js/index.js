/* Assignment Operators */
let x = 5;
let z = 7;

console.log(x);
console.log('---------------------------------');

// x = x + 1;
x += 1;
console.log(x);
console.log('---------------------------------');

// x = x - 2;
x -= 2;
console.log(x);
console.log('---------------------------------');

// x = x * 2;
x *= 2;
console.log(x);
console.log('---------------------------------');

// x = x / 4;
x /= 4;
console.log(x);
console.log('---------------------------------');

// z = z % 2;
x %= 2;
console.log('Exacto => ', x);
console.log('---------------------------------');

// z = z % 3;
z %= 3;
console.log('Inexacto => ', z);
console.log('---------------------------------');